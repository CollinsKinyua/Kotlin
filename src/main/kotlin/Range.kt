fun main(args: Array<String>) {
    print("Number Range:")

    for (num in 3..9){
        println(num)
    }

    for (num in 10 .. 15){
        println(num)
    }
     print("Character Range:")

    for (char in 'c' .. 'i' ){
        println(char)
    }
    for (char in 'M' .. 'w'){
        println(char)
    }
}